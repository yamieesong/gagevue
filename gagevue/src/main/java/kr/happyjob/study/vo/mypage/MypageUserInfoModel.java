package kr.happyjob.study.vo.mypage;

public class MypageUserInfoModel {
	// 게시판 글 번호
	private String mbr_no;
	private String name;
	private String mbr_hp;
	private String mbr_mail;
	private String loginID;
	private String password;
	private String mbr_yn;
	private String goal;
	
	public String getGoal() {
		return goal;
	}
	public void setGoal(String goal) {
		this.goal = goal;
	}
	public String getMbr_no() {
		return mbr_no;
	}
	public void setMbr_no(String mbr_no) {
		this.mbr_no = mbr_no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMbr_hp() {
		return mbr_hp;
	}
	public void setMbr_hp(String mbr_hp) {
		this.mbr_hp = mbr_hp;
	}
	public String getMbr_mail() {
		return mbr_mail;
	}
	public void setMbr_mail(String mbr_mail) {
		this.mbr_mail = mbr_mail;
	}
	public String getLoginID() {
		return loginID;
	}
	public void setLoginID(String loginID) {
		this.loginID = loginID;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getMbr_yn() {
		return mbr_yn;
	}
	public void setMbr_yn(String mbr_yn) {
		this.mbr_yn = mbr_yn;
	}
	
}
